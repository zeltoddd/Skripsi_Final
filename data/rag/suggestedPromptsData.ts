/**
 * Get random suggested prompts for a specific major
 */
export function getRandomSuggestedPrompts(jurusan: string, count: number = 4): string[] {
  const all = SUGGESTED_PROMPTS[jurusan.toLowerCase()] || SUGGESTED_PROMPTS['general'];
  const shuffled = [...all].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

/**
 * Session-based cache to keep prompts consistent throughout a session
 */
const sessionCache = new Map<string, string[]>();

/**
 * Get prompts for a specific session and major.
 * Prompts are cached per session to ensure consistency.
 */
export function getSessionPrompts(sessionId: string, jurusan: string): string[] {
  const cacheKey = `${sessionId}:${jurusan.toLowerCase()}`;
  
  if (!sessionCache.has(cacheKey)) {
    sessionCache.set(cacheKey, getRandomSuggestedPrompts(jurusan, 4));
  }
  
  return sessionCache.get(cacheKey)!;
}

/**
 * Clear prompt cache for a session (useful for refresh)
 */
export function clearPromptCache(sessionId?: string) {
  if (sessionId) {
    for (const key of sessionCache.keys()) {
      if (key.startsWith(sessionId)) {
        sessionCache.delete(key);
      }
    }
  } else {
    sessionCache.clear();
  }
}
